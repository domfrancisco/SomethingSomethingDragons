function toNullCardFields() {
  return {
    count: null,
    resource: null,
    boostCount: null,
    boostResource: null,
    count2: null,
    resource2: null,
    boostCount2: null,
    boostResource2: null,
    count3: null,
    resource3: null,
    count4: null,
    resource4: null,
  };
}

function withNullCardFields(card) {
  return {
    ...toNullCardFields(),
    ...card,
  };
}

